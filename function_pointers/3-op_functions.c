#include "3-calc.h"
#include <stdio.h>
/**
 * op_add - add
 * @a: first num argument
 * @b: second num argument
 * Return: Success
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtract
 * @a: first num argument
 * @b: second num argument
 * Return: Success
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiply
 * @a: first num argument
 * @b: second num argument
 * Return: Success
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divide
 * @a: first num argument
 * @b: second num argument
 * Return: Success
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - modulus
 * @a: first num argument
 * @b: second num argument
 * Return: Success
 */
int op_mod(int a, int b)
{
	return (a % b);
}
